    private void deleteMemoryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMemoryButtonActionPerformed
        if(BeMapEditor.serviceRoutine.getSerialConnection().sendDeleteRequest()==1){
            updateStatus("\nPoints successfully deleted");
            try {
                BeMapEditor.serviceRoutine.getSerialConnection().updateMemoryState();
            } catch (InterruptedException ex) {
                Logger.getLogger(statusWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else updateStatus("\nError: No points deleted");
    }//GEN-LAST:event_deleteMemoryButtonActionPerformed



